package day17_While_DoWhile;

public class WhileLoop {
    public static void main(String[] args) {

        if (true) {
            System.out.println("Wooden Spoon"); // 1 time
        }
        System.out.println("-----------------------------------------");

        while (true){
            System.out.println("Wooden Spoon");   // infinite times  wooden spoon write
        }




    }
}
