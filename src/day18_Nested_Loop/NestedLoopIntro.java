package day18_Nested_Loop;

public class NestedLoopIntro {
    public static void main(String[] args) {




        for (int j = 0; j < 4; j++) {
            System.out.println("Wooden spoon");

            for (int i = 0; i < 5; i++) {
                System.out.println("Wooden Spoon");
            }
        }








    }
}
