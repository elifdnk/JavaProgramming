package day18_Nested_Loop;

public class NestedLoopPractice2Square {
    public static void main(String[] args) {

        for (int j = 1; j <=10; j++) {
            for (int i = 1; i <=10 ; i++) {
                System.out.print("* ");

            }
            System.out.println();

        }



    }
}
